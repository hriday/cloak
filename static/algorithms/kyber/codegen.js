// Generates the Python ML-KEM demo shown on the Done step.
//
// The lesson walked the toy parameters q = 257, n = 4, k = 2 by hand. This
// final script bridges to *real* Kyber by calling `kyber-py`, a pure-Python
// FIPS 203 implementation of ML-KEM at the production parameters q = 3329,
// n = 256, k = 3 (ML-KEM-768 — the default in TLS 1.3 hybrid KEX).
//
// `kyber-py` is chosen over `pqcrypto` (the runner-up) because it's pure
// Python — no C toolchain, single pip install, works the same on macOS,
// Linux, and Windows. The header comment notes `pqcrypto` as the production-
// closer alternative for readers who want the NIST C reference under the
// hood. When `pyca/cryptography` ships ML-KEM as a stable API, the codegen
// can swap to it for consistency with the rest of the lessons on the site.
//
// If the page has frozen-vector values in state from the lesson walk (the
// canonical v[0] from step 5), we drop them in as comments so the script
// and the lesson cross-reference cleanly.

function comment(label, value) {
  if (value === undefined || value === null || value === "") return "";
  return `  # ${label}: ${value}`;
}

export function full_script(state) {
  const s = state || {};
  const vCoeff = s.kyber_v_coeff;

  return [
    "# Kyber / ML-KEM demo — generated by cloak.moosha.org",
    "#",
    "# Bridges the toy parameters from the lesson (q=257, n=4, k=2) to real",
    "# ML-KEM-768 (q=3329, n=256, k=3 — the default in TLS 1.3 hybrid KEX).",
    "#",
    "# Requires `kyber-py` (pure Python, no compiler): pip install kyber-py",
    "# Alternative:  pip install pqcrypto  — wraps the NIST C reference",
    "#               implementation; closer to what production crypto libraries",
    "#               link against, but needs a C toolchain to install.",
    "",
    "from kyber_py.ml_kem import ML_KEM_768",
    "",
    "# 1. Keygen. The library samples randomness internally; the encapsulation",
    "#    key (public) and decapsulation key (private) come back as bytes.",
    "ek, dk = ML_KEM_768.keygen()",
    "print(f\"public key:  {len(ek)} bytes\")    # 1184 bytes for ML-KEM-768",
    "print(f\"private key: {len(dk)} bytes\")    # 2400 bytes for ML-KEM-768",
    "",
    "# 2. Encapsulate (sender side). The output `K_sender` is the 32-byte",
    "#    shared secret; `ct` is the ciphertext sent across the wire.",
    "#    This corresponds to step 5 of the lesson — same shape, just",
    "#    bigger numbers and 256 message bits encoded in parallel instead of 1." +
      comment("page captured v[0]", vCoeff),
    "K_sender, ct = ML_KEM_768.encaps(ek)",
    "print(f\"ciphertext:  {len(ct)} bytes\")    # 1088 bytes for ML-KEM-768",
    "print(f\"shared key:  {K_sender.hex()}\")",
    "",
    "# 3. Decapsulate (recipient side). Runs `v - sᵀ·u` over the production",
    "#    ring and decodes the result back into the 32-byte shared secret.",
    "K_recipient = ML_KEM_768.decaps(dk, ct)",
    "print(f\"recovered:   {K_recipient.hex()}\")",
    "",
    "# 4. The whole point of the protocol: both sides land on identical bytes.",
    "assert K_sender == K_recipient, \"shared keys must agree\"",
    "",
    "# Production note: ML-KEM-768 wraps the toy CPAPKE construction we just",
    "# walked in the Fujisaki-Okamoto transform to lift CPA-security up to",
    "# IND-CCA2. That's what makes the real thing safe to deploy against an",
    "# attacker who can submit chosen ciphertexts — exactly what TLS faces.",
    "",
    "# Real-world deployment: TLS 1.3 uses the hybrid group `X25519MLKEM768`",
    "# (Chrome 124+, Firefox 132+, Cloudflare). Both X25519 and ML-KEM-768",
    "# secrets are derived independently, then HKDF-extracted together so the",
    "# combined secret is safe as long as either primitive holds.",
  ].join("\n");
}

