// Generates the Python SPHINCS+ (SLH-DSA) demo shown on the Done step.
//
// The lesson walked a *toy* construction by hand: 8 WOTS+ keypairs under a
// height-3 Merkle tree, n = 16 bytes (truncated SHA-256), w = 16. This
// script bridges to *real* SLH-DSA at the production parameters using the
// `pqcrypto` package, which wraps the NIST PQ-Crypto C reference. We use
// `sphincs_sha2_128f_simple` — 128-bit security, "fast" variant, ~17 KB
// signatures, the most common choice when SPHINCS+ is used in TLS or for
// CA root keys.
//
// The header notes alternative variants — `sphincs_sha2_128s_simple` for
// 7.8 KB signatures at the cost of much slower signing, and the 192/256
// security levels — so the reader can swap variants by changing the import.
//
// If the page captured signature bytes / leaf index / root from the
// interactive step, they're embedded as comments so the script and the
// lesson cross-reference cleanly.

function comment(label, value) {
  if (value === undefined || value === null || value === "") return "";
  return `  # ${label}: ${value}`;
}

export function full_script(state) {
  const s = state || {};
  const sigHex = s.sphincs_signature;
  const sigPreview = typeof sigHex === "string" && sigHex.length > 0
    ? sigHex.slice(0, 32) + "..." + sigHex.slice(-8)
    : null;
  const root = s.sphincs_root;
  const leafIdx = s.sphincs_leaf_index;
  const message = s.sphincs_last_input || "hello SPHINCS+";
  const messageLiteral = JSON.stringify(message);

  return [
    "# SPHINCS+ / SLH-DSA demo — generated by cloak.moosha.org",
    "#",
    "# Bridges the toy lesson (8 WOTS+ keys under a 3-deep Merkle tree, n=16)",
    "# to real SLH-DSA-SHA2-128f at the FIPS 205 parameters: a 63-deep hypertree",
    "# of 16-leaf Merkle trees with a FORS few-time signature at the bottom.",
    "# Same building blocks — WOTS+ chains, Merkle trees, hash-only security —",
    "# just *many* more of them.",
    "#",
    "# Requires `pqcrypto` (wraps the NIST PQ-Crypto C reference implementation):",
    "#   pip install pqcrypto",
    "#",
    "# Variants you can swap in:",
    "#   sphincs_sha2_128s_simple → 7856-byte sigs, slow sign (~1s), fast verify",
    "#   sphincs_sha2_128f_simple → 17088-byte sigs, faster sign (~10ms)  ← used here",
    "#   sphincs_sha2_192f_simple → ~35 KB sigs, NIST level 3",
    "#   sphincs_sha2_256f_simple → ~50 KB sigs, NIST level 5",
    "",
    "from pqcrypto.sign.sphincs_sha2_128f_simple import (",
    "    generate_keypair,",
    "    sign,",
    "    verify,",
    ")",
    "",
    "# 1. Keygen. Internally: a master seed drives a deterministic hypertree of",
    "#    WOTS+ + FORS keys; the public key is the root of the top Merkle tree." +
      comment("page captured root", root),
    "public_key, secret_key = generate_keypair()",
    "print(f\"public key:  {len(public_key)} bytes\")    # 32 bytes for 128f_simple",
    "print(f\"secret key:  {len(secret_key)} bytes\")    # 64 bytes for 128f_simple",
    "",
    `message = ${messageLiteral}.encode("utf-8")` +
      comment("page captured leaf index", leafIdx),
    "",
    "# 2. Sign. The library picks a randomised FORS instance + a hypertree path",
    "#    deterministically from H(secret_key_prf || message). All WOTS+ chains",
    "#    along the path are signed; each Merkle root above gets a WOTS+ sig of",
    "#    its own, climbing 63 layers to the public-key root.",
    "signature = sign(secret_key, message)",
    "print(f\"signature:   {len(signature)} bytes\")    # 17088 bytes for 128f_simple" +
      (sigPreview ? "\n  # page captured sig preview: " + sigPreview : ""),
    "",
    "# 3. Verify. Reconstruct each WOTS+ public-key element from the signature",
    "#    chunks, hash to a leaf, climb the Merkle path with the auth siblings,",
    "#    re-hash to the next-layer leaf, climb that tree, repeat 63 times, and",
    "#    compare the top root to public_key. Returns True / raises on mismatch.",
    "ok = verify(public_key, message, signature)",
    'print("\\u2713 signature verified" if ok else "\\u2717 verification failed")',
    "",
    "# 4. Show a tampered signature is rejected (Merkle root mismatch).",
    "tampered = bytearray(signature)",
    "tampered[0] ^= 0x01",
    "try:",
    "    verify(public_key, message, bytes(tampered))",
    '    print("\\u2717 tampered signature accepted — should not happen!")',
    "except Exception as e:",
    '    print(f"\\u2713 tampered signature rejected: {type(e).__name__}")',
    "",
    "# Why SPHINCS+ at all, given the ~17 KB signature?",
    "#",
    "#   Security relies on ONE assumption: SHA-256 is collision- and",
    "#   preimage-resistant. There's no lattice, no number-theoretic, no",
    "#   discrete-log structure to break. If a quantum (or classical)",
    "#   attack ever breaks Dilithium / Falcon / ECDSA, SPHINCS+ keeps",
    "#   working — which is exactly the property you want for a root-CA",
    "#   signing key that's rotated every 10+ years.",
    "#",
    "#   Cloudflare experiments with SPHINCS+ for long-lived signing keys.",
    "#   The X.509 OID for SLH-DSA-SHA2-128f is 2.16.840.1.101.3.4.3.20.",
    "#   IETF draft-ietf-lamps-pq-composite-sigs combines it with ECDSA for",
    "#   hybrid certificates during the PQ migration.",
  ].join("\n");
}

