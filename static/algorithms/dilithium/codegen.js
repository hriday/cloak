// Generates the Python ML-DSA demo shown on the Done step.
//
// The lesson walked the toy parameters q = 257, n = 4, k = 2 by hand. This
// final script bridges to *real* Dilithium by calling `dilithium-py`, a pure-
// Python FIPS 204 implementation of ML-DSA at the production parameters
// q = 8380417, n = 256, with k, l varying by parameter set.
//
// `dilithium-py` is chosen for the same reasons `kyber-py` was chosen on the
// Kyber lesson — pure Python, single pip install, no C toolchain. The header
// comment notes `pqcrypto` as the production-closer alternative for readers
// who want the NIST C reference under the hood. When pyca/cryptography ships
// ML-DSA as a stable API the codegen can swap to it for consistency.
//
// If the page captured `dilithium_signature` in state from the in-browser
// demo, we drop the hex into a comment so the lesson and the script
// cross-reference cleanly.

function comment(label, value) {
  if (value === undefined || value === null || value === "") return "";
  // Truncate long hex blobs for readability — full sig hex is 80 chars,
  // which fits inline. Real ML-DSA sigs are 6,618 hex chars — definitely
  // truncate those if a real path ever lands here.
  const display = String(value).length > 100 ? String(value).slice(0, 96) + "..." : String(value);
  return `  # ${label}: ${display}`;
}

export function full_script(state) {
  const s = state || {};
  const sigHex = s.dilithium_signature;
  const message = s.dilithium_last_input || "hello world";
  const messageLiteral = JSON.stringify(message);

  return [
    "# Dilithium / ML-DSA demo — generated by cloak.moosha.org",
    "#",
    "# Bridges the toy parameters from the lesson (q=257, n=4, k=2) to real",
    "# ML-DSA-65 (q=8380417, n=256, k=6, l=5 — the security target most",
    "# deployments will land on, ~192-bit classical security).",
    "#",
    "# Requires `dilithium-py` (pure Python, no compiler): pip install dilithium-py",
    "# Alternative:  pip install pqcrypto  — wraps the NIST C reference",
    "#               implementation; closer to what production crypto libraries",
    "#               link against, but needs a C toolchain to install.",
    "",
    "from dilithium_py.ml_dsa import ML_DSA_65",
    "",
    "# 1. Keygen. The library samples randomness internally; the public key",
    "#    (verification key) and private key (signing key) come back as bytes.",
    "pk, sk = ML_DSA_65.keygen()",
    "print(f\"public key:  {len(pk)} bytes\")    # 1952 bytes for ML-DSA-65",
    "print(f\"private key: {len(sk)} bytes\")    # 4032 bytes for ML-DSA-65",
    "",
    `message = ${messageLiteral}.encode("utf-8")`,
    "",
    "# 2. Sign. ML-DSA signing runs the same rejection-sampling loop you walked",
    "#    in step 4 — sample y, derive c = H(message, w), check ||z||_inf is",
    "#    short, restart if not. Real Dilithium typically needs 4-7 attempts",
    "#    per signature; the loop is invisible to the caller.",
    "#" + comment("toy signature from the browser (z||c||w hex)", sigHex),
    "signature = ML_DSA_65.sign(sk, message)",
    "print(f\"signature:   {len(signature)} bytes\")    # 3309 bytes for ML-DSA-65",
    "",
    "# 3. Verify a fresh signature — returns bool. Real ML-DSA-65 verify is",
    "#    one matrix-vector multiply, one polynomial subtract, one hash, and",
    "#    a couple of norm checks — all in the NTT domain for speed.",
    "ok = ML_DSA_65.verify(pk, message, signature)",
    "assert ok, \"fresh signature must verify\"",
    "print(\"\\u2713 fresh signature verified\")",
    "",
    "# 4. Verify a TAMPERED signature — flip one byte and watch it fail.",
    "tampered = bytearray(signature)",
    "tampered[0] ^= 0x01",
    "ok = ML_DSA_65.verify(pk, message, bytes(tampered))",
    "assert not ok, \"tampered signature must NOT verify\"",
    "print(\"\\u2713 tampered signature rejected\")",
    "",
    "# 5. Verify against a WRONG message — also fails. The challenge c is",
    "#    derived from (message, w); changing the message changes c, breaks",
    "#    the algebra A·z − c·t ≈ w that verification depends on.",
    "ok = ML_DSA_65.verify(pk, b\"different message\", signature)",
    "assert not ok, \"wrong-message verify must fail\"",
    "print(\"\\u2713 wrong-message verify rejected\")",
    "",
    "# Notice: signing the same message twice produces *different* bytes.",
    "# That's the rejection-sampling loop — fresh y each attempt — and the",
    "# OPPOSITE of Ed25519's deterministic signatures. Both are intentional",
    "# design choices; both are safe.",
    "sig2 = ML_DSA_65.sign(sk, message)",
    "print(f\"signing twice produces different bytes: {signature != sig2}\")",
    "",
    "# Size tax vs Ed25519:",
    "#   Ed25519 public key:  32 bytes;  ML-DSA-65 public key:  1952 bytes  (61x larger)",
    "#   Ed25519 signature:   64 bytes;  ML-DSA-65 signature:   3309 bytes  (52x larger)",
    "# That's the cost of post-quantum security. Deployments use a hybrid",
    "# (Ed25519 + ML-DSA together) so the connection survives if EITHER",
    "# primitive holds — same pattern as Kyber + X25519 in TLS 1.3 KEX.",
  ].join("\n");
}

