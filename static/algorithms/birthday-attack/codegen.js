// Generates the Python birthday-attack demo shown on the Done step.
//
// Pedagogy. The browser ran a brute-force collision search on
// SHA-256 truncated to 16 bits and reported the attempt count. This
// Python script:
//
//   1. Reproduces the exact scenario locally with `hashlib.sha256`
//      and a dict — the analog of our JS Map.
//   2. Runs the search across SEVERAL hash sizes (8, 16, 24-bit) so
//      the reader can watch the attempt count grow as ~sqrt(N).
//   3. Prints a reference table of expected work for real-world hash
//      sizes (16, 32, 64, 80, 128, 256 bits) — the same table the
//      `more-bits-more-work` lesson step showed.
//
// The script is single-file, depends only on stdlib (`hashlib`,
// `math`, `secrets`), and runs in under a second for the small hash
// sizes it searches at runtime.

function _quote(s) {
  // Safe Python string literal — backslashes/quotes escaped by JSON.
  return JSON.stringify(String(s));
}

export function full_script(state) {
  const s = state || {};

  // State written by the find_collision validator. Fall back to
  // sensible placeholders when the user opens the Done step before
  // running step 3 (shouldn't happen via the wizard, but the script
  // should still execute).
  const inputA = typeof s.bday_collision_a === "string" ? s.bday_collision_a : "abcd1234";
  const inputB = typeof s.bday_collision_b === "string" ? s.bday_collision_b : "wxyz5678";
  const collisionHash = typeof s.bday_collision_hash === "string"
    ? s.bday_collision_hash
    : "0000";
  const attempts = Number.isInteger(s.bday_attempts) ? s.bday_attempts : 256;
  const hashBits = Number.isInteger(s.bday_hash_bits) ? s.bday_hash_bits : 16;

  const lines = [];

  lines.push(
    '"""',
    "Birthday-attack demo — generated by cloak.moosha.org.",
    "",
    "FOR LEARNING ONLY. Demonstrates the generic sqrt(N) birthday attack",
    "on N-output hash functions by brute-forcing a collision in a TINY",
    "truncated SHA-256 (16 bits by default). The same math, applied to",
    "MD5 (128 bits), is why MD5 is considered broken — sqrt(2^128) = 2^64",
    "work is feasible for a well-funded adversary (and has been demonstrated:",
    "Marc Stevens et al., 2008, 2012, 2017).",
    "",
    "The script:",
    "  1. Reproduces the lesson's in-browser collision search in pure Python.",
    "  2. Repeats the search at hash sizes 8, 16, and 24 bits so you can",
    "     watch attempts grow as ~sqrt(N).",
    "  3. Prints a reference table of expected work for real-world hash",
    "     output sizes (16, 32, 64, 80, 128, 256 bits).",
    '"""',
    "",
    "from __future__ import annotations",
    "import hashlib",
    "import math",
    "import secrets",
    "",
    "",
    "# =========================================================================",
    "# The collision finder.",
    "#",
    "# Pure brute force: generate random inputs, hash with SHA-256, truncate",
    "# to `hash_bits` bits, store in a dict keyed by truncated hash. On a",
    "# repeat key with a different input, we've found a collision.",
    "# =========================================================================",
    "",
    "def truncated_sha256(message: bytes, hash_bits: int) -> bytes:",
    "    \"\"\"Return the first `hash_bits` bits of SHA-256(message) as bytes.\"\"\"",
    "    full = hashlib.sha256(message).digest()",
    "    full_bytes = (hash_bits + 7) // 8",
    "    out = bytearray(full[:full_bytes])",
    "    rem = hash_bits % 8",
    "    if rem:",
    "        mask = (0xFF << (8 - rem)) & 0xFF",
    "        out[-1] &= mask",
    "    return bytes(out)",
    "",
    "",
    "def find_collision(hash_bits: int, max_attempts: int = 1_000_000):",
    "    \"\"\"Brute-force a collision in SHA-256 truncated to `hash_bits` bits.\"\"\"",
    "    seen: dict[bytes, bytes] = {}",
    "    for attempt in range(1, max_attempts + 1):",
    "        # 8 random bytes is plenty of input variety for any hash_bits <= 32.",
    "        inp = secrets.token_hex(4).encode('ascii')",
    "        h = truncated_sha256(inp, hash_bits)",
    "        prior = seen.get(h)",
    "        if prior is not None and prior != inp:",
    "            return {",
    "                'inputA': prior,",
    "                'inputB': inp,",
    "                'hash': h.hex(),",
    "                'attempts': attempt,",
    "            }",
    "        if prior is None:",
    "            seen[h] = inp",
    "    raise RuntimeError(",
    "        f'no collision after {max_attempts} attempts — hash_bits too large for brute force'",
    "    )",
    "",
    "",
    "def expected_attempts(hash_bits: int) -> float:",
    "    \"\"\"1.18 * sqrt(2^hash_bits) — the median-collision birthday bound.\"\"\"",
    "    return 1.18 * math.sqrt(2 ** hash_bits)",
    "",
    "",
    "# =========================================================================",
    "# Reference table — what the birthday bound looks like at real hash sizes.",
    "# =========================================================================",
    "",
    "_TABLE = [",
    "    (16,  'toy (this script)',     '~ instant'),",
    "    (32,  'short truncation',      '~ 1 second on a laptop'),",
    "    (64,  '64-bit hash',           '~ 10 minutes on a GPU'),",
    "    (80,  '80-bit (SHA-1 quasi)',  'distributed compute / specialized HW'),",
    "    (128, '128-bit (MD5)',         'nation-state budget (proven feasible)'),",
    "    (256, '256-bit (SHA-256)',     'infeasible for any conceivable adversary'),",
    "]",
    "",
    "",
    "def print_reference_table() -> None:",
    "    print('hash bits | expected work (sqrt(N))      | notes')",
    "    print('----------+-------------------------------+----------------------------------')",
    "    for bits, label, notes in _TABLE:",
    "        work = expected_attempts(bits)",
    "        print(f'{bits:>4}      | {work:>22.3g} attempts | {label}: {notes}')",
    "",
    "",
    "# =========================================================================",
    "# Run the demo.",
    "# =========================================================================",
    "",
    `# The collision the browser found in step 3 of the lesson — for reference:`,
    `#   inputA  = ${_quote(inputA)}`,
    `#   inputB  = ${_quote(inputB)}`,
    `#   hash16  = ${_quote(collisionHash)}   (first ${hashBits} bits of SHA-256)`,
    `#   attempts = ${attempts}   (expected ~${Math.round(1.18 * Math.sqrt(2 ** hashBits))} by birthday math)`,
    "",
    "",
    "def main() -> None:",
    "    print_reference_table()",
    "    print()",
    "    for hb in (8, 16, 24):",
    "        result = find_collision(hb)",
    "        print(",
    "            f'hash_bits={hb:>2}  '",
    "            f'expected~{expected_attempts(hb):.0f}  '",
    "            f'actual={result[\"attempts\"]:>6}  '",
    "            f'inputs={result[\"inputA\"]!r} vs {result[\"inputB\"]!r}  '",
    "            f'collision_hash={result[\"hash\"]}'",
    "        )",
    "    print()",
    "    print('Takeaway: an n-bit hash gives only n/2 bits of COLLISION resistance.')",
    "    print('Pick a hash with output >= 2x your target security level. SHA-256 for 128-bit.')",
    "",
    "",
    "if __name__ == '__main__':",
    "    main()",
  );

  return lines.join("\n");
}

