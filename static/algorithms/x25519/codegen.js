// Generates the Python X25519 demo shown on the Done step.
//
// The pedagogy: the in-page lesson runs the exchange through Web Crypto.
// This script shows the production-grade equivalent in Python via
// `cryptography.hazmat.primitives.asymmetric.x25519` — the same library
// every modern Python TLS / SSH / WireGuard tool reaches for.
//
// If the page captured live hex blobs in state (from the live Web Crypto
// path or the RFC 7748 fallback), we drop them in as comments next to the
// corresponding script lines so the downloaded script and the page the
// learner just walked cross-reference cleanly.

function comment(label, hex) {
  if (!hex) return "";
  return `  # ${label}: ${hex}`;
}

export function full_script(state) {
  const s = state || {};
  const alicePub = s.x25_alice_pub || "";
  const bobPub   = s.x25_bob_pub || "";
  const shared   = s.x25_shared_secret || "";

  return [
    "# X25519 (ECDH on Curve25519) demo — generated by cloak.moosha.org",
    "# Requires the `cryptography` package: pip install cryptography",
    "",
    "from cryptography.hazmat.primitives.asymmetric.x25519 import X25519PrivateKey",
    "from cryptography.hazmat.primitives import serialization",
    "",
    "# 1. Each side generates a fresh keypair. Private keys are 32 random bytes,",
    "#    clamped per RFC 7748 §5 (the library handles the clamping for you).",
    "alice_priv = X25519PrivateKey.generate()",
    "bob_priv   = X25519PrivateKey.generate()",
    "",
    "# 2. Export the 32-byte raw public keys to send across the wire.",
    "alice_pub_bytes = alice_priv.public_key().public_bytes(",
    "    encoding=serialization.Encoding.Raw,",
    "    format=serialization.PublicFormat.Raw,",
    ")" + comment("page captured Alice pub", alicePub),
    "bob_pub_bytes = bob_priv.public_key().public_bytes(",
    "    encoding=serialization.Encoding.Raw,",
    "    format=serialization.PublicFormat.Raw,",
    ")" + comment("page captured Bob pub", bobPub),
    "",
    "# 3. Each side independently derives the same 32-byte shared secret",
    "#    by combining their private key with the other's public key.",
    "shared_a = alice_priv.exchange(bob_priv.public_key())",
    "shared_b = bob_priv.exchange(alice_priv.public_key())" +
      comment("page captured shared", shared),
    "",
    "# 4. The whole point of the protocol: both sides land on identical bytes.",
    'assert shared_a == shared_b, "shared secrets must agree"',
    "",
    'print("Alice pub :", alice_pub_bytes.hex())',
    'print("Bob pub   :", bob_pub_bytes.hex())',
    'print("shared    :", shared_a.hex())',
    "",
    "# Production note: never key a symmetric cipher with the raw X25519",
    "# output. Always run it through a KDF first (HKDF-Expand with a context",
    "# string) — the X25519 shared secret has structure a real key shouldn't.",
  ].join("\n");
}

