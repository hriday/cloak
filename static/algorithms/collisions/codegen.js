// Generates the Python hashlib MD5 + SHA-1 collision demo shown on the
// Done step.
//
// Pedagogy. The JS lesson hashes the Wang/Stevens MD5 pair in-page and
// confirms the digests collide. This script reproduces the lesson in
// standalone Python — runnable on any Python 3 install, no third-party
// packages — so the reader can see `True` in their own terminal.
//
// The SHA-1 portion shows the SHAttered published hash and (commented) the
// shasum command to verify against the real downloaded PDFs.

// The full Wang/Stevens MD5 colliding pair — same hex constants as
// coll_demo.js. We carry them inline so the generated Python is
// self-contained.
const MD5_BLOB_1_HEX =
  "d131dd02c5e6eec4693d9a0698aff95c2fcab58712467eab4004583eb8fb7f89" +
  "55ad340609f4b30283e488832571415a085125e8f7cdc99fd91dbdf280373c5b" +
  "d8823e3156348f5bae6dacd436c919c6dd53e2b487da03fd02396306d248cda0" +
  "e99f33420f577ee8ce54b67080a80d1ec69821bcb6a8839396f9652b6ff72a70";

const MD5_BLOB_2_HEX =
  "d131dd02c5e6eec4693d9a0698aff95c2fcab50712467eab4004583eb8fb7f89" +
  "55ad340609f4b30283e4888325f1415a085125e8f7cdc99fd91dbd7280373c5b" +
  "d8823e3156348f5bae6dacd436c919c6dd53e23487da03fd02396306d248cda0" +
  "e99f33420f577ee8ce54b67080280d1ec69821bcb6a8839396f965ab6ff72a70";

const MD5_COLLISION_HASH = "79054025255fb1a26e4bc422aef54eb4";
const SHATTERED_HASH     = "38762cf7f55934b34d179ae6a4c80cadccbb7f0a";

export function full_script(_state) {
  return [
    '"""',
    "Hash collision demo — generated by cloak.moosha.org.",
    "",
    "Demonstrates two real, published collisions:",
    "",
    "  1. MD5 — Wang/Stevens (2004/2007). Two 128-byte messages with the",
    "     same MD5 digest. Used by Flame malware (2012) to forge a Microsoft",
    "     Windows Update certificate.",
    "",
    "  2. SHA-1 — SHAttered (Stevens et al., Google + CWI, 2017). Two",
    "     422,435-byte PDF files with the same SHA-1 digest. The colliding",
    "     PDFs are downloadable from https://shattered.io; this script",
    "     shows the published hash and the command to verify against the",
    "     real files.",
    "",
    "Don't use MD5 or SHA-1 for ANY cryptographic purpose. Use SHA-256,",
    "SHA-3, or BLAKE2/3.",
    '"""',
    "",
    "import hashlib",
    "",
    "",
    "# =========================================================================",
    "# MD5 collision (Wang & Yu, EUROCRYPT 2005; canonical demo pair).",
    "# Two 128-byte messages differing in exactly 6 bytes (positions 19, 45,",
    "# 59, 83, 109, 123) that hash to the SAME MD5 digest.",
    "# =========================================================================",
    "",
    `BLOB_1 = bytes.fromhex(`,
    `    "${MD5_BLOB_1_HEX.slice(0, 64)}"`,
    `    "${MD5_BLOB_1_HEX.slice(64, 128)}"`,
    `    "${MD5_BLOB_1_HEX.slice(128, 192)}"`,
    `    "${MD5_BLOB_1_HEX.slice(192, 256)}"`,
    ")",
    "",
    `BLOB_2 = bytes.fromhex(`,
    `    "${MD5_BLOB_2_HEX.slice(0, 64)}"`,
    `    "${MD5_BLOB_2_HEX.slice(64, 128)}"`,
    `    "${MD5_BLOB_2_HEX.slice(128, 192)}"`,
    `    "${MD5_BLOB_2_HEX.slice(192, 256)}"`,
    ")",
    "",
    "h1 = hashlib.md5(BLOB_1).hexdigest()",
    "h2 = hashlib.md5(BLOB_2).hexdigest()",
    "",
    'print(f"md5(BLOB_1) = {h1}")',
    'print(f"md5(BLOB_2) = {h2}")',
    'print(f"BLOB_1 == BLOB_2:        {BLOB_1 == BLOB_2}")',
    'print(f"md5(BLOB_1) == md5(BLOB_2): {h1 == h2}")',
    "",
    "# How many bytes do they actually differ in?",
    "diff = sum(1 for a, b in zip(BLOB_1, BLOB_2) if a != b)",
    'print(f"BLOB_1 and BLOB_2 differ in {diff} byte positions")',
    "",
    "# The load-bearing assertion. If this fires, hashlib.md5 disagrees",
    "# with the published Wang/Stevens collision — your Python install is",
    "# broken (or someone has rewritten history).",
    `assert h1 == h2 == "${MD5_COLLISION_HASH}", (`,
    `    f"expected both MD5s to equal ${MD5_COLLISION_HASH}, got {h1} and {h2}"`,
    ")",
    'print()',
    'print("MD5 collision confirmed: two different inputs, same fingerprint.")',
    'print()',
    "",
    "",
    "# =========================================================================",
    "# SHA-1 collision (SHAttered, Stevens et al., 2017).",
    "# The colliding PDFs are 422 KB each — too big to embed in this script.",
    "# Download from https://shattered.io and verify against the published",
    "# hash below. Both PDFs hash to the same 160-bit SHA-1 digest.",
    "# =========================================================================",
    "",
    `SHATTERED_PUBLISHED = "${SHATTERED_HASH}"`,
    "",
    'print(f"SHAttered published SHA-1: {SHATTERED_PUBLISHED}")',
    'print("To verify locally:")',
    'print("    curl -O https://shattered.io/static/shattered-1.pdf")',
    'print("    curl -O https://shattered.io/static/shattered-2.pdf")',
    'print("    shasum -a 1 shattered-1.pdf shattered-2.pdf")',
    'print()',
    'print("Both PDFs render as visibly different images.")',
    'print("Both have identical SHA-1.")',
    'print(f"Both: {SHATTERED_PUBLISHED}")',
    'print()',
    "",
    "# Sanity-check that hashlib.sha1 is wired up — `abc` is the FIPS 180-4",
    "# test vector. (We can't verify the SHAttered hash without the PDFs.)",
    "sha1_abc = hashlib.sha1(b\"abc\").hexdigest()",
    'assert sha1_abc == "a9993e364706816aba3e25717850c26c9cd0d89d"',
    "",
    "",
    "# =========================================================================",
    "# Bottom line.",
    "# =========================================================================",
    "",
    'print("Bottom line:")',
    'print("  - MD5 collisions cost seconds on a laptop. Do not use MD5.")',
    'print("  - SHA-1 collisions cost ~$110K of GPU time (SHAttered, 2017),")',
    'print("    or $45K of cloud time (SHA-1 is a Shambles, 2020). Do not use SHA-1.")',
    'print("  - Use SHA-256, SHA-3, or BLAKE2/3 — collision-finding for these")',
    'print("    would require ~2^128 work, outside any practical compute budget.")',
  ].join("\n");
}

