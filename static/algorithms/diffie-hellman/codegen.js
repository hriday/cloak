// Generates the Python Diffie-Hellman demo shown on the Done step.
//
// The pedagogy: the in-page lesson walked the user through a toy exchange
// with p=23, g=5 — small enough to compute by hand. This script shows the
// production-grade equivalent in Python via
// `cryptography.hazmat.primitives.asymmetric.dh` — what real systems use
// when they need classical mod-p DH (rare now; ECDH/X25519 is the modern
// default, but classical DH still appears in legacy TLS, IPsec, and SSH).
//
// We render the toy numbers from state as a leading comment block so the
// downloaded script cross-references the lesson the learner just walked.

export function full_script(state) {
  const s = state || {};
  const aSec = s.dh_alice_secret != null ? s.dh_alice_secret : 6;
  const bSec = s.dh_bob_secret   != null ? s.dh_bob_secret   : 15;
  const shared = s.dh_shared     != null ? s.dh_shared       : 2;

  return [
    "# Diffie-Hellman (classical) demo — generated by cloak.moosha.org",
    "# Requires the `cryptography` package: pip install cryptography",
    "",
    "# In the lesson you walked the toy exchange:",
    "#   p = 23, g = 5",
    "#   Alice's secret a = " + aSec + " → A = 5^" + aSec + " mod 23 = 8",
    "#   Bob's   secret b = " + bSec + " → B = 5^" + bSec + " mod 23 = 19",
    "#   shared s = 19^" + aSec + " mod 23 = " + shared + " (= 8^" + bSec + " mod 23)",
    "#",
    "# Real DH uses a 2048-bit (or larger) prime and generator g=2. The toy",
    "# lesson uses g=5 because the table 5^k mod 23 is short enough to walk",
    "# by hand; pyca/cryptography only supports g=2 or g=5, and production",
    "# code overwhelmingly uses g=2 with an RFC 3526 well-known prime.",
    "",
    "from cryptography.hazmat.primitives.asymmetric import dh",
    "from cryptography.hazmat.primitives import serialization",
    "",
    "# 1. Generate (or load) DH parameters: a 2048-bit prime p and generator g=2.",
    "#    In production, prefer a well-known RFC 3526 group (Group 14 = 2048-bit",
    "#    MODP) over per-peer parameter generation — it lets peers skip parameter",
    "#    negotiation, and prevents a malicious peer from picking a backdoored",
    "#    prime. The library accepts pre-baked parameters via",
    "#    `dh.DHParameterNumbers(p_const, g=2).parameters()`.",
    "parameters = dh.generate_parameters(generator=2, key_size=2048)",
    "",
    "# 2. Each party generates a private key and derives a public key from",
    "#    the shared parameters.",
    "alice_priv = parameters.generate_private_key()",
    "bob_priv   = parameters.generate_private_key()",
    "",
    "alice_pub = alice_priv.public_key()",
    "bob_pub   = bob_priv.public_key()",
    "",
    "# 3. Each side computes the shared secret using their own private key",
    "#    and the peer's public key. They must agree byte-for-byte.",
    "alice_shared = alice_priv.exchange(bob_pub)",
    "bob_shared   = bob_priv.exchange(alice_pub)",
    'assert alice_shared == bob_shared, "DH agreement failed"',
    "",
    'print("Alice pub :", alice_pub.public_bytes(',
    "    encoding=serialization.Encoding.PEM,",
    "    format=serialization.PublicFormat.SubjectPublicKeyInfo,",
    ").decode())",
    'print("Bob pub   :", bob_pub.public_bytes(',
    "    encoding=serialization.Encoding.PEM,",
    "    format=serialization.PublicFormat.SubjectPublicKeyInfo,",
    ").decode())",
    'print("shared    :", alice_shared.hex())',
    "",
    "# Production note: never use the raw DH shared secret as a symmetric key.",
    "# Run it through HKDF (or another KDF) to derive AES / ChaCha20 / HMAC",
    "# keys — the raw secret has algebraic structure a real key shouldn't.",
  ].join("\n");
}

