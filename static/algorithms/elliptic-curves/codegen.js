// Python codegen for the "Elliptic curves, visually" Done step.
//
// Two halves:
//   (1) A from-scratch toy: pointAdd / pointDouble / scalarMul implemented
//       directly on `y² = x³ + x + 6 mod 11` — the same curve the ECDSA
//       lesson uses. We print a worked 2G/3G/.../12G/13G table so the
//       learner sees the orbit collapse to the point at infinity.
//   (2) The pyca/cryptography one-liner: ec.generate_private_key(SECP256R1())
//       — the production library. Same primitives, real curve.
//
// Captured state.ec_scalar_k / ec_scalar_kp (from the scalar-mult step) are
// dropped in as comments so the downloaded script cross-references the
// lesson the learner just walked.

function comment(label, val) {
  if (val === undefined || val === null) return "";
  return `  # ${label}: ${val}`;
}

export function full_script(state) {
  const s = state || {};
  const userK = s.ec_scalar_k;
  const userKP = s.ec_scalar_kp
    ? `(${s.ec_scalar_kp.x.toFixed(3)}, ${s.ec_scalar_kp.y.toFixed(3)})`
    : null;

  return [
    "# Elliptic curves — generated by cloak.moosha.org",
    "#",
    "# Part 1: hand-rolled point arithmetic on a tiny curve. No libraries.",
    "# This is exactly what every ECC implementation does under the hood,",
    "# scaled up to 256-bit prime fields.",
    "#",
    "# Part 2: the same primitives via pyca/cryptography on a real curve.",
    "#",
    "# Prerequisites:",
    "#   pip install cryptography      # only needed for Part 2",
    "",
    "# ============================================================",
    "# PART 1 — toy curve, from scratch",
    "# ============================================================",
    "#",
    "# Curve: y² = x³ + x + 6  (mod 11)",
    "# Base point G = (2, 7), prime order n = 13.",
    "# (Same curve the ECDSA lesson uses — Stallings §10.4.)",
    "",
    "A, B, P = 1, 6, 11",
    "G = (2, 7)",
    "",
    "def modinv(a, m):",
    "    \"\"\"Extended Euclidean modular inverse.\"\"\"",
    "    a %= m",
    "    if a == 0: raise ZeroDivisionError(f'no inverse for 0 mod {m}')",
    "    old_r, r = a, m",
    "    old_s, s = 1, 0",
    "    while r != 0:",
    "        q = old_r // r",
    "        old_r, r = r, old_r - q*r",
    "        old_s, s = s, old_s - q*s",
    "    return old_s % m",
    "",
    "def point_add(p1, p2):",
    "    \"\"\"Group operation on the short-Weierstrass curve.\"\"\"",
    "    if p1 is None: return p2",
    "    if p2 is None: return p1",
    "    x1, y1 = p1",
    "    x2, y2 = p2",
    "    if x1 == x2:",
    "        if (y1 + y2) % P == 0:",
    "            return None                              # P + (-P) = O",
    "        return point_double(p1)",
    "    s = (y2 - y1) * modinv(x2 - x1, P) % P",
    "    x3 = (s*s - x1 - x2) % P",
    "    y3 = (s*(x1 - x3) - y1) % P",
    "    return (x3, y3)",
    "",
    "def point_double(p):",
    "    \"\"\"Tangent-line slope at p; same return shape as point_add.\"\"\"",
    "    if p is None: return None",
    "    x, y = p",
    "    if y == 0: return None                            # vertical tangent",
    "    s = (3*x*x + A) * modinv(2*y, P) % P",
    "    x3 = (s*s - 2*x) % P",
    "    y3 = (s*(x - x3) - y) % P",
    "    return (x3, y3)",
    "",
    "def scalar_mul(k, p):",
    "    \"\"\"Double-and-add. Runs in O(log k) point operations.\"\"\"",
    "    result, addend = None, p",
    "    while k > 0:",
    "        if k & 1:",
    "            result = point_add(result, addend)",
    "        addend = point_double(addend)",
    "        k >>= 1",
    "    return result",
    "",
    "# Walk the full 13-element subgroup generated by G.",
    "# Notice 13·G = the point at infinity (None) — that's the order.",
    'print("k  | k·G")',
    'print("---+------------")',
    "for k in range(1, 14):",
    "    kp = scalar_mul(k, G)",
    "    print(f\"{k:2} | {kp}\")",
    "",
    userK !== undefined
      ? `# On the visual lesson you computed ${userK}·P on y² = x³ - 3x + 5 (over the reals).\n# Result there: ${userKP}.  (Real-number arithmetic isn't what production uses;\n# this toy script does the same algorithm in F_11 instead.)\n`
      : "",
    "# ============================================================",
    "# PART 2 — the same primitives, on a real curve",
    "# ============================================================",
    "#",
    "# pyca/cryptography wraps point arithmetic on the NIST curves",
    "# (and secp256k1) so application code never touches the formulas.",
    "# Everything below runs on the same group operations you just",
    "# implemented in Part 1 — except A, B, P are now hundreds of digits.",
    "",
    "from cryptography.hazmat.primitives.asymmetric import ec",
    "from cryptography.hazmat.primitives import serialization",
    "",
    "# Generate a P-256 keypair. Under the hood the library picks a random",
    "# 256-bit scalar d, computes Q = d·G, returns (d, Q) as a key object.",
    "private_key = ec.generate_private_key(ec.SECP256R1())",
    "public_key = private_key.public_key()",
    "",
    "# The public key is just a curve point — 32-byte x + 1 sign bit, encoded",
    "# in 33-byte SEC1 compressed form. Same encoding TLS certs use.",
    "public_bytes = public_key.public_bytes(",
    "    encoding=serialization.Encoding.X962,",
    "    format=serialization.PublicFormat.CompressedPoint,",
    ")",
    'print()',
    'print("P-256 public key (33-byte compressed SEC1):", public_bytes.hex())',
    "",
    "# Real curves available out of the box — pick the one your protocol needs:",
    "#   ec.SECP256R1()   NIST P-256 — default for TLS, JWT ES256",
    "#   ec.SECP384R1()   NIST P-384 — high-assurance government",
    "#   ec.SECP521R1()   NIST P-521 — FIPS-overkill",
    "#   ec.SECP256K1()   the Bitcoin / Ethereum curve",
    "#",
    "# All four use the *same* point-add / point-double / scalar-mul you",
    "# just hand-rolled above — only the field prime and the curve params change.",
    "#",
    "# Forward links:",
    "#   ECDH (X25519, P-256 ECDH)  uses scalar_mul to derive a shared secret.",
    "#   Ed25519 / ECDSA / Schnorr  use scalar_mul + group addition to sign.",
    "# All three lessons assume you can picture k·P. You now can.",
  ].filter(Boolean).join("\n");
}

