// Python codegen for the Schnorr Done step.
//
// Mirrors the in-browser toy-group demo: implement Schnorr from scratch on
// the same p=467, g=4, q=233 group so the learner can paste the script,
// run it locally, and watch the same arithmetic produce the same R, s, and
// verification equation as the page.
//
// We could call out to `coincurve` / `secp256k1-py` for BIP-340 production
// Schnorr (Bitcoin Taproot), but the brief asks for the pedagogical
// version — the math is identical, only the group changes. The comment
// at the top of the script forward-links to the production library.

export function full_script(state) {
  const message = (state && state.schnorr_message) || "ship it";
  const messageLiteral = JSON.stringify(message);
  return [
    "# Schnorr signatures (toy group) — generated by cloak.moosha.org",
    "#",
    "# This is the pedagogical implementation walked in the lesson, in pure",
    "# Python. Same group as the in-browser demo: p=467, g=4, q=233. The math",
    "# is structurally identical to BIP-340 Schnorr on secp256k1 (Bitcoin",
    "# Taproot since 2021) — only the group changes. For production, install",
    "# `coincurve` and use coincurve.PrivateKey.sign_schnorr / verify_schnorr,",
    "# or `secp256k1-py`. The Schnorr scheme itself is the same four lines.",
    "#",
    "# Prerequisites: only the standard library (hashlib, secrets).",
    "",
    "import hashlib",
    "import secrets",
    "",
    "# Toy discrete-log group.",
    "P = 467     # field prime",
    "G = 4       # generator of the order-q subgroup",
    "Q = 233     # subgroup order (prime; (P-1) / Q = 2)",
    "",
    "def hash_challenge(R: int, X: int, message: bytes) -> int:",
    '    """SHA-256(R ‖ X ‖ message) mod Q, with R and X as 2-byte big-endian."""',
    "    h = hashlib.sha256()",
    "    h.update(R.to_bytes(2, 'big'))",
    "    h.update(X.to_bytes(2, 'big'))",
    "    h.update(message)",
    "    return int.from_bytes(h.digest(), 'big') % Q",
    "",
    "def keygen() -> tuple[int, int]:",
    '    """Sample x ∈ [1, Q-1] and derive X = g^x mod p."""',
    "    x = secrets.randbelow(Q - 1) + 1",
    "    X = pow(G, x, P)",
    "    return x, X",
    "",
    "def sign(x: int, message: bytes) -> tuple[int, int]:",
    '    """Schnorr sign — pick a nonce, build the commitment, hash, respond."""',
    "    k = secrets.randbelow(Q - 1) + 1    # never reuse k across signatures",
    "    R = pow(G, k, P)                    # commitment",
    "    X = pow(G, x, P)                    # public key (recomputed for the hash)",
    "    e = hash_challenge(R, X, message)   # challenge",
    "    s = (k + e * x) % Q                 # response — linear in the secret",
    "    return R, s",
    "",
    "def verify(X: int, message: bytes, signature: tuple[int, int]) -> bool:",
    '    """Verify g^s == R · X^e (mod p)."""',
    "    R, s = signature",
    "    if not (0 < R < P) or not (0 <= s < Q):",
    "        return False",
    "    e = hash_challenge(R, X, message)",
    "    lhs = pow(G, s, P)",
    "    rhs = (R * pow(X, e, P)) % P",
    "    return lhs == rhs",
    "",
    "# --- Demo --------------------------------------------------------------",
    "",
    "x, X = keygen()",
    'print(f"private key x = {x}")',
    'print(f"public  key X = g^x mod p = {X}")',
    "",
    `message = ${messageLiteral}.encode("utf-8")`,
    "R, s = sign(x, message)",
    'print(f"signature: R = {R}, s = {s}")',
    "",
    "print(\"verify (fresh):\", verify(X, message, (R, s)))     # True",
    "print(\"verify (tampered s):\", verify(X, message, (R, (s + 1) % Q)))  # False",
    'print("verify (tampered message):", verify(X, b"different", (R, s)))  # False',
    "",
    "# --- The pedagogical superpower: linearity / MuSig --------------------",
    "#",
    "# Schnorr signatures are linear in the secret key. Alice and Bob can",
    "# jointly sign with combined key X_AB = X_A · X_B without trusting a",
    "# dealer — each picks their own nonce, publishes the commitment, and",
    "# adds their share of s. The verifier can't tell a joint signature from",
    "# a single-party one. ECDSA can't do this without expensive MPC.",
    "",
    "x_A, X_A = keygen()",
    "x_B, X_B = keygen()",
    "k_A = secrets.randbelow(Q - 1) + 1",
    "k_B = secrets.randbelow(Q - 1) + 1",
    "R_A = pow(G, k_A, P)",
    "R_B = pow(G, k_B, P)",
    "",
    "X_joint = (X_A * X_B) % P                 # combined public key",
    "R_joint = (R_A * R_B) % P                 # combined commitment",
    "e = hash_challenge(R_joint, X_joint, message)",
    "s_A = (k_A + e * x_A) % Q",
    "s_B = (k_B + e * x_B) % Q",
    "s_joint = (s_A + s_B) % Q                 # combined response",
    "",
    'print("multi-sig verify:", verify(X_joint, message, (R_joint, s_joint)))  # True',
    "",
    "# That's MuSig in 12 lines. Real MuSig (Maxwell-Poelstra-Seurin-Wuille,",
    "# 2018) adds a key-aggregation hash to prevent rogue-key attacks, but",
    "# the algebraic core is exactly the above. FROST (threshold Schnorr)",
    "# generalizes the same trick to t-of-n signing without a trusted dealer.",
  ].join("\n");
}

