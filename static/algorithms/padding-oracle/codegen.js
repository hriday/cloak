// Generates the Python padding-oracle demo shown on the Done step.
//
// The pedagogy: the JS lesson runs the attack against an in-page simulator.
// This script reproduces the lesson end-to-end in standalone Python — a
// vulnerable mock server (`decrypt_or_error`) + the same byte-by-byte
// attack (`attack_block`) — so the learner can copy-paste it into a venv,
// run it, watch it work, then poke at the code to see how the trick works
// in a language they probably read more fluently than JavaScript.
//
// The downloaded script intentionally:
//   - Inlines the target ciphertext hex from page state, so the script
//     and the page the learner just walked cross-reference cleanly.
//   - Carries heavy comments — this is teaching material first, working
//     code second.
//   - Warns at the top NEVER to deploy unauthenticated CBC. The whole
//     point of the lesson is that the server you just built is broken.

function _hexLiteral(hex) {
  // Always render a quoted, lowercased hex string with leading zeros
  // preserved. Falls back to an empty placeholder if the value isn't
  // available yet (e.g. step 7 reached without running step 4).
  if (typeof hex === "string" && /^[0-9a-fA-F]+$/.test(hex)) {
    return `"${hex.toLowerCase()}"`;
  }
  return `"00112233445566778899aabbccddeeff"`;
}

export function full_script(state) {
  const s = state || {};
  const ctHex = _hexLiteral(s.po_target_block_hex);
  const ivHex = _hexLiteral(s.po_target_iv_hex);
  const recoveredPt = s.po_plaintext || s.po_recovered_byte_hex || null;
  const totalQueries =
    typeof s.po_total_queries === "number" ? s.po_total_queries : null;

  const provenance = [];
  if (recoveredPt) {
    provenance.push(`# Page recovered: ${JSON.stringify(recoveredPt)}`);
  }
  if (totalQueries !== null) {
    provenance.push(`# Page used roughly ${totalQueries} oracle queries.`);
  }

  return [
    '"""',
    "Padding-oracle attack demo — generated by cloak.moosha.org.",
    "",
    "FOR LEARNING ONLY. Never deploy unauthenticated CBC like this. The whole",
    "point of the lesson is that the server below is broken: by exposing the",
    "padding-check result as a distinguishable response, it leaks one bit of",
    "information per query — and that one bit, queried ~2048 times, decrypts",
    "an entire block without the attacker ever holding the key.",
    "",
    "Mitigation: use AEAD (AES-GCM, ChaCha20-Poly1305). Any tampering is",
    "detected before the padding check would run, so there's no padding oracle",
    "to attack.",
    '"""',
    "",
    "from cryptography.hazmat.primitives.ciphers import Cipher, algorithms, modes",
    "from cryptography.hazmat.primitives.padding import PKCS7",
    "",
    "# ---- the vulnerable server (mock) ----",
    "#",
    "# A real padding-oracle server would (a) decrypt, (b) check padding,",
    "# (c) check MAC, (d) process the request — and would return a",
    "# distinguishable error for each failure mode (or leak the difference",
    "# via timing). Below we collapse that to a function that returns",
    '# "ok" or "bad_padding".',
    "",
    "KEY = bytes.fromhex(\"00112233445566778899aabbccddeeff\")  # the server's secret",
    "",
    "",
    "def encrypt(iv: bytes, plaintext: bytes) -> bytes:",
    '    """One-block AES-CBC encryption with PKCS7. Server-side helper."""',
    "    padder = PKCS7(128).padder()",
    "    padded = padder.update(plaintext) + padder.finalize()",
    "    enc = Cipher(algorithms.AES(KEY), modes.CBC(iv)).encryptor()",
    "    return enc.update(padded) + enc.finalize()",
    "",
    "",
    "def decrypt_or_error(iv: bytes, ct: bytes) -> str:",
    '    """The vulnerable oracle. Returns "ok" or "bad_padding".',
    "",
    "    The vulnerability is *not* the padding check — the check is correct.",
    "    The vulnerability is exposing its result to the attacker as a",
    "    distinguishable response, instead of folding it into a generic",
    '    "decryption failed" that also covers MAC mismatch, format errors, etc.',
    '    """',
    "    if len(iv) != 16 or len(ct) % 16 != 0 or len(ct) == 0:",
    '        return "bad_padding"',
    "    dec = Cipher(algorithms.AES(KEY), modes.CBC(iv)).decryptor()",
    "    pt = dec.update(ct) + dec.finalize()",
    "    # Naive PKCS7 check, identical to the JS simulator.",
    "    last = pt[-1]",
    "    if last < 1 or last > 16:",
    '        return "bad_padding"',
    "    if pt[-last:] != bytes([last]) * last:",
    '        return "bad_padding"',
    '    return "ok"',
    "",
    "",
    "# ---- the attack ----",
    "#",
    "# The attacker has only: the target ciphertext block C (16 bytes), the",
    "# real IV (16 bytes), and the oracle API. No key. No plaintext.",
    "",
    "",
    "def recover_byte(iv: bytes, ct: bytes, target_pos: int, known: bytes) -> int:",
    '    """Recover plaintext byte at target_pos (0..15) using ~128 queries average.',
    "",
    "    `known` holds the already-recovered plaintext bytes for positions",
    "    target_pos+1 .. 15 (lower indices can be zero-filled).",
    '    """',
    "    P = 16 - target_pos          # PKCS7 length we're forging the suffix to",
    "    forged = bytearray(iv)       # start from the real IV",
    "    # Set positions > target_pos so the decrypted plaintext ends in P repeated.",
    "    for i in range(target_pos + 1, 16):",
    "        forged[i] = iv[i] ^ known[i] ^ P",
    "    for candidate in range(256):",
    "        forged[target_pos] = candidate",
    "        resp = decrypt_or_error(bytes(forged), ct)",
    '        if resp == "bad_padding":',
    "            continue",
    "        # P=1 false-positive: the plaintext might end in 0x02 0x02 by",
    "        # accident. Perturb the second-to-last forged-IV byte and re-query.",
    "        if P == 1 and target_pos > 0:",
    "            perturbed = bytearray(forged)",
    "            perturbed[target_pos - 1] ^= 0xFF",
    '            if decrypt_or_error(bytes(perturbed), ct) == "bad_padding":',
    "                continue   # was 0x02 0x02 — keep sweeping",
    "        # Real hit. Derive the plaintext byte.",
    "        return candidate ^ P ^ iv[target_pos]",
    '    raise RuntimeError(f"no valid padding found at position {target_pos}")',
    "",
    "",
    "def recover_block(iv: bytes, ct: bytes) -> bytes:",
    '    """Recover all 16 bytes by inducting last-to-first."""',
    "    known = bytearray(16)",
    "    for i in range(15, -1, -1):",
    "        known[i] = recover_byte(iv, ct, i, bytes(known))",
    "    return bytes(known)",
    "",
    "",
    'if __name__ == "__main__":',
    "    # The page captured the following from the lesson run:",
    `    IV = bytes.fromhex(${ivHex})`,
    `    CT = bytes.fromhex(${ctHex})`,
    ...(provenance.length ? ["", ...provenance, ""] : [""]),
    "    recovered = recover_block(IV, CT)",
    '    print("recovered plaintext bytes (hex):", recovered.hex())',
    "    try:",
    '        print("recovered plaintext (utf-8):  ", recovered.decode("utf-8"))',
    "    except UnicodeDecodeError:",
    '        print("recovered plaintext not valid UTF-8")',
    "",
    "    # Sanity: if you happen to know what the server encrypted, re-encrypt",
    "    # under the same key/IV and confirm the ciphertext round-trips.",
    "    assert encrypt(IV, recovered) == CT, \"recovered plaintext re-encrypts to CT\"",
    '    print("\\u2713 attack succeeded — no key was ever used by the attacker.")',
  ].join("\n");
}

