export const pick_pq      = (s) => `p, q = ${s.p}, ${s.q}`;
export const compute_n    = (s) => `n = p * q  # n = ${s.n}`;
export const compute_phi  = (s) => `phi = (p - 1) * (q - 1)  # phi = ${s.phi}`;
export const pick_e       = (s) => `e = ${s.e}  # gcd(e, phi) == 1`;
export const compute_d    = (s) => `d = pow(e, -1, phi)  # d = ${s.d}`;
export const pick_message = (s) => `m = ${s.m}`;
export const encrypt      = (s) => `c = pow(m, e, n)  # c = ${s.c}`;
export const decrypt      = (s) => `m_decrypted = pow(c, d, n)  # m_decrypted = ${s.m_decrypted}`;
export const info         = (_s) => "";

export function encode_sentence(state) {
  return `encrypted = [pow(ord(ch), ${state.e2}, ${state.n2}) for ch in sentence]`;
}

const _ORDER = [
  { fn: pick_pq,      keys: ["p", "q"] },
  { fn: compute_n,    keys: ["n"] },
  { fn: compute_phi,  keys: ["phi"] },
  { fn: pick_e,       keys: ["e"] },
  { fn: compute_d,    keys: ["d"] },
  { fn: pick_message, keys: ["m"] },
  { fn: encrypt,      keys: ["c"] },
  { fn: decrypt,      keys: ["m_decrypted"] },
];

export function full_script(state) {
  const lines = ["# RSA lesson — generated by cloak.moosha.org", ""];
  for (const { fn, keys } of _ORDER) {
    if (keys.every((k) => k in state)) lines.push(fn(state));
  }
  if (state.sentence !== undefined && state.e2 !== undefined) {
    lines.push("");
    lines.push("# --- Encrypt a real sentence ---");
    lines.push(`p2, q2 = ${state.p2}, ${state.q2}`);
    lines.push(`n2 = ${state.n2}`);
    lines.push(`phi2 = (p2 - 1) * (q2 - 1)  # ${state.phi2}`);
    lines.push(`e2 = ${state.e2}`);
    lines.push(`d2 = ${state.d2}  # modinv(e2, phi2)`);
    lines.push(`sentence = ${JSON.stringify(state.sentence)}`);
    lines.push(`encrypted = [pow(ord(ch), e2, n2) for ch in sentence]`);
    lines.push(`decrypted = "".join(chr(pow(c, d2, n2)) for c in encrypted)`);
    lines.push(`assert decrypted == sentence`);
  }
  return lines.join("\n") + "\n";
}
