// Generates the Python ChaCha20-Poly1305 demo shown on the Done step.
//
// Uses pyca's `cryptography` library, which bundles the AEAD in
// `cryptography.hazmat.primitives.ciphers.aead.ChaCha20Poly1305`. Its
// `.encrypt()` returns `ciphertext || tag` concatenated; we show how to
// split them apart in the demo so the bytes line up with what the
// browser panel displayed.

export function full_script(state) {
  const messageLiteral = JSON.stringify(state.chap_message ?? "hello chacha");
  const aadValue = state.chap_aad ?? "";
  const aadLiteral = JSON.stringify(aadValue);
  // aad is bytes in the API. Empty AAD shown as b"" for readability.
  return [
    "# ChaCha20-Poly1305 AEAD demo — generated by cloak.moosha.org",
    "# Requires the `cryptography` package: pip install cryptography",
    "",
    "import os",
    "from cryptography.hazmat.primitives.ciphers.aead import ChaCha20Poly1305",
    "from cryptography.exceptions import InvalidTag",
    "",
    "# 1. Generate a fresh random 32-byte key + 12-byte nonce.",
    "#    NEVER reuse a (key, nonce) pair — that breaks Poly1305's",
    "#    one-time-MAC security property and leaks the keystream.",
    "key = ChaCha20Poly1305.generate_key()      # 32 bytes",
    "nonce = os.urandom(12)                      # 96-bit nonce",
    "aead = ChaCha20Poly1305(key)",
    "",
    "# 2. Encrypt. Associated data is authenticated but NOT encrypted —",
    "#    think: a public header that an attacker mustn't be able to forge.",
    `message = b${messageLiteral}`,
    `aad = b${aadLiteral}`,
    "ct = aead.encrypt(nonce, message, aad)      # ct = ciphertext || tag",
    "",
    "# 3. Decrypt to confirm. The library re-verifies the tag internally;",
    "#    decrypt() raises InvalidTag if tampering is detected.",
    "pt = aead.decrypt(nonce, ct, aad)",
    "assert pt == message",
    "",
    "print(\"key      :\", key.hex())",
    "print(\"nonce    :\", nonce.hex())",
    "print(\"aad      :\", aad.decode() if aad else \"(empty)\")",
    "print(\"ct||tag  :\", ct.hex())",
    "print(\"  ct     :\", ct[:-16].hex())        # last 16 bytes are the tag",
    "print(\"  tag    :\", ct[-16:].hex())",
    "print(\"decrypted:\", pt.decode())",
    "",
    "# 4. Tamper detection — flip one byte of the ciphertext and watch",
    "#    decrypt() raise InvalidTag. The 16-byte tag is what catches this.",
    "tampered = bytearray(ct)",
    "tampered[0] ^= 0x01",
    "try:",
    "    aead.decrypt(nonce, bytes(tampered), aad)",
    "    print(\"BUG: tampered ciphertext was accepted\")",
    "except InvalidTag:",
    "    print(\"tamper   : InvalidTag (caught — authentication works)\")",
  ].join("\n");
}

