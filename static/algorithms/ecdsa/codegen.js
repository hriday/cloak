// Python codegen for the ECDSA Done step.
//
// Pedagogical mirror of the in-browser lesson, but on a *real* curve:
// generate a P-256 ECDSA keypair, sign a message, verify a fresh signature,
// then watch a tampered signature raise InvalidSignature. Uses pyca's
// cryptography.hazmat — the canonical production library in Python land.
//
// One important note baked into the comments: pyca's `.sign()` uses
// deterministic-k via RFC 6979 internally, so the PS3 disaster the
// lesson just walked is structurally impossible with this library —
// even though it's still classical ECDSA on the wire.

export function full_script(state) {
  const message = (state && state.ecdsa_message) || "ship it";
  const messageLiteral = JSON.stringify(message);
  return [
    "# ECDSA in production — generated by cloak.moosha.org",
    "#",
    "# Real ECDSA on NIST P-256 (secp256r1) — the curve TLS server certs",
    "# and JWT ES256 use. Generate a keypair, sign a message, verify the",
    "# signature, then verify a tampered version and watch InvalidSignature",
    "# fire.",
    "#",
    "# IMPORTANT: pyca/cryptography uses RFC 6979 deterministic-k under the",
    "# hood, so the per-signature nonce is derived from HMAC-SHA256(d, e)",
    "# rather than the OS RNG. Same key + same message ⇒ same signature.",
    "# That's the structural defense against the Sony PS3 nonce-reuse attack",
    "# the previous step walked through — even though the wire format is",
    "# still classical ECDSA.",
    "#",
    "# Prerequisites:",
    "#   pip install cryptography",
    "",
    "from cryptography.hazmat.primitives.asymmetric import ec",
    "from cryptography.hazmat.primitives import hashes, serialization",
    "from cryptography.exceptions import InvalidSignature",
    "",
    "# Keygen — pick the curve, get a private key whose scalar is sampled",
    "# uniformly from [1, n-1]. SECP256R1 == NIST P-256.",
    "private_key = ec.generate_private_key(ec.SECP256R1())",
    "public_key = private_key.public_key()",
    "",
    "# Show the public key in compressed SEC1 form (one byte sign + 32-byte",
    "# x-coordinate = 33 bytes total). The same encoding TLS certificates use.",
    "public_bytes = public_key.public_bytes(",
    "    encoding=serialization.Encoding.X962,",
    "    format=serialization.PublicFormat.CompressedPoint,",
    ")",
    'print("public key (hex):", public_bytes.hex())',
    "",
    `message = ${messageLiteral}.encode("utf-8")`,
    "",
    "# Sign. pyca picks k via RFC 6979 deterministic-k — no RNG involved.",
    "# Signature is DER-encoded (r, s); roughly 70–72 bytes for P-256.",
    "signature = private_key.sign(message, ec.ECDSA(hashes.SHA256()))",
    'print("signature (hex):", signature.hex())',
    'print("signature length:", len(signature), "bytes  # DER, varies 70-72")',
    "",
    "# Verify a fresh signature — returns None on success, raises on failure.",
    "public_key.verify(signature, message, ec.ECDSA(hashes.SHA256()))",
    'print("\\u2713 fresh signature verified")',
    "",
    "# Verify a TAMPERED signature — flip the last byte and watch it fail.",
    "tampered = bytearray(signature)",
    "tampered[-1] ^= 0x01",
    "try:",
    "    public_key.verify(bytes(tampered), message, ec.ECDSA(hashes.SHA256()))",
    '    print("\\u2717 tampered signature accepted — should not happen!")',
    "except InvalidSignature:",
    '    print("\\u2713 tampered signature rejected (InvalidSignature)")',
    "",
    "# To sign the same message under a different curve, swap the curve",
    "# constructor: ec.SECP384R1() for high-assurance TLS, ec.SECP256K1()",
    "# for Bitcoin/Ethereum, ec.SECP521R1() for FIPS-overkill. The ECDSA",
    "# algorithm itself is identical — pick the curve, you get a signature.",
  ].join("\n");
}

