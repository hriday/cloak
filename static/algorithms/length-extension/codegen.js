// Generates the Python length-extension demo shown on the Done step.
//
// Pedagogy. The JS lesson runs a real attack against an in-page server.
// This script reproduces the lesson end-to-end in standalone Python so
// the reader can copy-paste it into a venv, run it, watch the server's
// own verifier accept a forged signature, then read the SHA-256 source
// to understand WHY it works.
//
// Why the script ships its own SHA-256. Python's hashlib.sha256 doesn't
// expose the chaining state — same problem we had with Web Crypto. So
// the script inlines a small reference SHA-256 (~80 lines) with
// .from_state(state, total_bytes) so .update + .finalize can resume
// from a captured mid-stream state. Cross-checked against hashlib in
// the script's own test block.
//
// The downloaded script intentionally:
//   - Inlines the (request, sig) pair from page state so the user can
//     watch the same attack they just ran.
//   - Carries heavy comments — teaching material first.
//   - Warns at the top NEVER to use H(secret||msg) for authentication.

function _quote(s) {
  // Safe Python string literal: use JSON-like double-quotes; backslashes
  // and quotes get escaped by JSON.stringify.
  return JSON.stringify(String(s));
}

function _hexLiteralOr(hex, fallback) {
  if (typeof hex === "string" && /^[0-9a-fA-F]+$/.test(hex)) {
    return `"${hex.toLowerCase()}"`;
  }
  return `"${fallback}"`;
}

