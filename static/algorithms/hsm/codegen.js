// Generates the Python AWS KMS demo shown on the Done step.
//
// The pedagogy: the JS lesson uses Web Crypto's non-extractable keys to
// simulate an HSM. This script shows the production equivalent — AWS KMS,
// where the key material lives on AWS-managed HSMs and never leaves the
// Amazon datacenter. The application calls kms.encrypt / kms.decrypt;
// AWS does the actual cryptography.

export function full_script(state) {
  const message = (state && state.hsm_message) || "hello from the application";
  const messageLiteral = JSON.stringify(message);
  return [
    "# HSM in production — generated by cloak.moosha.org",
    "#",
    "# This is how you'd talk to a real HSM in production: AWS KMS.",
    "# The key material lives on AWS-managed HSMs and never leaves",
    "# Amazon's datacenter. You call kms.encrypt and kms.decrypt;",
    "# AWS does the actual cryptography.",
    "#",
    "# Prerequisites:",
    "#   pip install boto3",
    "#   AWS credentials configured (e.g. ~/.aws/credentials or env vars",
    "#     AWS_ACCESS_KEY_ID / AWS_SECRET_ACCESS_KEY)",
    "#   A KMS key ARN (create one in the AWS Console under KMS)",
    "",
    "import boto3",
    "",
    "# Replace with your own KMS key ARN.",
    'KEY_ID = "arn:aws:kms:us-east-1:123456789012:key/REPLACE-WITH-YOUR-KEY-ID"',
    `PLAINTEXT = ${messageLiteral}`,
    "",
    'kms = boto3.client("kms", region_name="us-east-1")',
    "",
    "# Encrypt — the plaintext goes to AWS, AWS returns ciphertext.",
    "# We never see the key.",
    "encrypt_response = kms.encrypt(",
    "    KeyId=KEY_ID,",
    '    Plaintext=PLAINTEXT.encode("utf-8"),',
    ")",
    'ciphertext_blob = encrypt_response["CiphertextBlob"]',
    'print("ciphertext (hex):", ciphertext_blob.hex())',
    "",
    "# Decrypt — AWS looks up which key produced this blob,",
    "# performs the decryption inside the HSM, and returns the plaintext.",
    "decrypt_response = kms.decrypt(CiphertextBlob=ciphertext_blob)",
    'decrypted = decrypt_response["Plaintext"].decode("utf-8")',
    "",
    "assert decrypted == PLAINTEXT",
    'print("\\u2713 Roundtrip OK:", decrypted)',
    "",
    "# Notice how we never touched the key material. That's the whole point",
    "# of an HSM — even the application talking to it cannot extract the key.",
    "# The key is born inside the HSM, lives inside the HSM, and dies inside",
    "# the HSM. All we can do is ask it to encrypt or decrypt on our behalf.",
  ].join("\n");
}

