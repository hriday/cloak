// Python codegen for the RSA-PSS Done step.
//
// Pedagogical mirror of the in-browser Web Crypto demo: generate a 2048-bit
// RSA keypair, sign a message with PSS+SHA256+salt32, verify the fresh
// signature, then sign the SAME message a second time and assert the two
// signatures differ — that's the probabilistic property RSA-PSS exists to
// provide. Finally, tamper one byte and watch InvalidSignature fire.

export function full_script(state) {
  const message = (state && state.rsapss_message) || "sign me";
  const messageLiteral = JSON.stringify(message);
  return [
    "# RSA-PSS in production — generated by cloak.moosha.org",
    "#",
    "# This is the production equivalent of the in-browser demo: generate",
    "# a 2048-bit RSA keypair, sign with PSS padding + SHA-256 + 32-byte",
    "# salt, verify the fresh signature, prove the scheme is probabilistic",
    "# by signing the same message twice and watching the bytes differ,",
    "# then flip one byte to trigger InvalidSignature.",
    "#",
    "# Prerequisites:",
    "#   pip install cryptography",
    "",
    "from cryptography.hazmat.primitives.asymmetric import rsa, padding",
    "from cryptography.hazmat.primitives import hashes, serialization",
    "from cryptography.exceptions import InvalidSignature",
    "",
    "# Keygen — sample two ~1024-bit primes, multiply to get the 2048-bit",
    "# modulus n, derive private exponent d from (p-1)(q-1) and e=65537.",
    "# Slow: ~100ms on a modern laptop because prime hunting is rejection",
    "# sampling. Ed25519 keygen is 10,000x faster.",
    "private_key = rsa.generate_private_key(public_exponent=65537, key_size=2048)",
    "public_key = private_key.public_key()",
    "",
    "# Show the public key in SubjectPublicKeyInfo (SPKI) DER form — the",
    "# canonical encoding, matches Web Crypto's exportKey('spki', publicKey).",
    "public_bytes = public_key.public_bytes(",
    "    encoding=serialization.Encoding.DER,",
    "    format=serialization.PublicFormat.SubjectPublicKeyInfo,",
    ")",
    'print("public key (SPKI hex):", public_bytes.hex())',
    'print("public key length:", len(public_bytes), "bytes")  # ~294 bytes',
    "",
    `message = ${messageLiteral}.encode("utf-8")`,
    "",
    "# Sign with PSS padding. MGF1+SHA-256 is the standard mask-generation",
    "# function; salt_length=32 matches the hash length (recommended default).",
    "# pyca exposes padding.PSS.MAX_LENGTH as a convenience for 'as much salt",
    "# as the modulus has room for'; 32 bytes is what most production code uses.",
    "pss_padding = padding.PSS(",
    "    mgf=padding.MGF1(hashes.SHA256()),",
    "    salt_length=32,",
    ")",
    "signature = private_key.sign(message, pss_padding, hashes.SHA256())",
    'print("signature (hex):", signature.hex())',
    'print("signature length:", len(signature), "bytes")  # always 256 (= 2048/8)',
    "",
    "# Verify a fresh signature — returns None on success, raises on failure.",
    "public_key.verify(signature, message, pss_padding, hashes.SHA256())",
    'print("\\u2713 fresh signature verified")',
    "",
    "# Probabilistic property: sign the SAME message AGAIN. The salt is",
    "# freshly random each call, so the bytes are completely different —",
    "# but BOTH signatures verify. (Unlike Ed25519, which is deterministic.)",
    "signature2 = private_key.sign(message, pss_padding, hashes.SHA256())",
    "assert signature != signature2, \"RSA-PSS should be probabilistic!\"",
    'print("\\u2713 second signature differs from first (probabilistic):")',
    'print("  sig 1:", signature.hex()[:64], "...")',
    'print("  sig 2:", signature2.hex()[:64], "...")',
    "public_key.verify(signature2, message, pss_padding, hashes.SHA256())",
    'print("\\u2713 second signature also verifies")',
    "",
    "# Tampered signature — flip one byte and watch it fail.",
    "tampered = bytearray(signature)",
    "tampered[0] ^= 0x01",
    "try:",
    "    public_key.verify(bytes(tampered), message, pss_padding, hashes.SHA256())",
    '    print("\\u2717 tampered signature accepted — should not happen!")',
    "except InvalidSignature:",
    '    print("\\u2713 tampered signature rejected (InvalidSignature)")',
    "",
    "# Where this runs in production: TLS 1.3 server CertificateVerify",
    "# (cipher suite TLS_AES_128_GCM_SHA256, signature algorithm",
    "# rsa_pss_rsae_sha256), most X.509 CA roots, S/MIME, FIPS 186-5",
    "# federal PKI signatures. The legacy PKCS#1 v1.5 sign mode that",
    "# preceded PSS is the one the Bleichenbacher attacks target — TLS",
    "# 1.3 forbids it for CertificateVerify; only PSS is allowed.",
  ].join("\n");
}

