// Generates the Python bcrypt demo shown on the Done step of the bcrypt lesson.
//
// Unlike the password-hashing lesson's codegen (which covers Argon2 + bcrypt
// side by side), this one is bcrypt-only and goes deeper: it varies the cost
// factor, demonstrates that the same password + cost produces DIFFERENT
// hashes because the salt is random, and verifies that `bcrypt.checkpw`
// works regardless because the salt is embedded in the hash string.

export function full_script(state) {
  const cost = Number(state?.bcrypt_cost);
  const usedCost = Number.isInteger(cost) && cost >= 4 && cost <= 31 ? cost : 12;

  return [
    "# bcrypt demo — generated by cloak.moosha.org",
    "#",
    "# The bcrypt Python library is a thin wrapper around the canonical C",
    "# implementation from OpenBSD. Install once:",
    "#",
    "#   pip install bcrypt",
    "#",
    "# Why bcrypt: 25 years of cryptanalysis, no known practical attacks, and",
    "# the salt + cost + hash all travel together in a single PHC-format",
    "# string ($2b$<cost>$<22 salt chars><31 hash chars>) — so storage is one",
    "# column per user in your DB. No separate salt column needed.",
    "#",
    "# For new systems, Argon2id is the modern default (PHC 2015 winner,",
    "# memory-hard). bcrypt is the right choice when you already have it",
    "# deployed, want maximum maturity, or need a single-file dependency.",
    "",
    "import bcrypt",
    "import time",
    "",
    'PASSWORD = b"correct-horse-battery-staple"',
    'WRONG    = b"Tr0ub4dor&3"',
    "",
    "# --- Hash once at the chosen cost ----------------------------------",
    "# `rounds` is the bcrypt cost factor: cost c means 2^c iterations of",
    "# the Blowfish key schedule. Each +1 doubles the work. 2024 default is",
    "# 12; bump to 13 or 14 if you can afford the latency at login.",
    `COST = ${usedCost}`,
    "",
    "t0 = time.perf_counter()",
    "hash_a = bcrypt.hashpw(PASSWORD, bcrypt.gensalt(rounds=COST))",
    "t1 = time.perf_counter()",
    'print(f"hash at cost {COST}: {hash_a.decode()}")',
    'print(f"  took {(t1 - t0) * 1000:.1f} ms")',
    "",
    "# --- Same password + same cost = DIFFERENT hash --------------------",
    "# Because bcrypt.gensalt() returns a fresh random 16-byte salt every",
    "# call, hashing the same password twice yields two different strings.",
    "# The 22 salt characters after the cost block will differ; the 31",
    "# hash characters at the end will differ. checkpw still works because",
    "# the salt is embedded in the hash, so it knows which salt to re-use.",
    "hash_b = bcrypt.hashpw(PASSWORD, bcrypt.gensalt(rounds=COST))",
    'print(f"hash again      : {hash_b.decode()}")',
    'assert hash_a != hash_b, "salt should be random per hash"',
    'print("\\u2713 same password + same cost gave two different hashes (random salt)")',
    "",
    "# --- Verify-success: both hashes still match the original password ",
    "# bcrypt.checkpw parses the embedded cost + salt out of the stored",
    "# hash and re-derives. No salt argument needed.",
    "assert bcrypt.checkpw(PASSWORD, hash_a) is True",
    "assert bcrypt.checkpw(PASSWORD, hash_b) is True",
    'print("\\u2713 bcrypt.checkpw accepts both hashes for the right password")',
    "",
    "# --- Verify-fail: wrong password returns False (no exception) ------",
    "assert bcrypt.checkpw(WRONG, hash_a) is False",
    'print("\\u2713 bcrypt.checkpw rejects the wrong password")',
    "",
    "# --- Demonstrate the exponential cost curve ------------------------",
    "# Hash the same password at three different costs and print the wall",
    "# time. Each +2 to the cost should ~4x the time (since 2^2 = 4).",
    "print()",
    'print("cost  ms       hash (truncated)")',
    'print("----  -------  ----------------------------")',
    "for c in (4, 8, 10, 12):",
    "    t0 = time.perf_counter()",
    "    h = bcrypt.hashpw(PASSWORD, bcrypt.gensalt(rounds=c))",
    "    elapsed_ms = (time.perf_counter() - t0) * 1000",
    '    print(f"{c:>4}  {elapsed_ms:>7.1f}  {h.decode()[:40]}...")',
    "",
    "# That's bcrypt end to end: hash once with a random salt, store the",
    "# whole $2b$... string, re-derive with checkpw at login. The 72-byte",
    "# password cap and lack of memory-hardness are real footguns — see",
    "# the lesson text for when to pick Argon2id instead.",
  ].join("\n");
}

