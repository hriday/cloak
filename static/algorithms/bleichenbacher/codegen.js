// Generates the Python Bleichenbacher demo shown on the Done step.
//
// The downloaded script:
//   - Implements toy RSA from scratch (modPow + extended Euclidean for d).
//   - Defines a vulnerable PKCS#1 v1.5 decrypt — returns "conforming" or
//     "bad" exactly like the in-page simulator.
//   - Implements Bleichenbacher's Step 1–4 algorithm against that
//     vulnerable decrypt.
//   - Inlines the toy primes p, q, modulus n, exponent e, and the target
//     ciphertext from page state so the script and the lesson cross-
//     reference cleanly.
//   - Carries heavy comments — this is teaching material first, working
//     code second.
//   - Warns at the top NEVER to use PKCS#1 v1.5 for new code. The whole
//     point of the lesson is that v1.5 is broken in this exact way.

function _intLiteral(x, fallback) {
  if (typeof x === "string" && /^-?\d+$/.test(x)) return x;
  if (typeof x === "number" && Number.isFinite(x)) return String(x);
  if (typeof x === "bigint") return x.toString();
  return String(fallback);
}

export function full_script(state) {
  const s = state || {};

  // Defaults are the values baked into bleich_config.js so the script
  // works even on the Done step before the user has actually run the
  // attack. (Reaching Done implies they ran step 5, but defensively.)
  const nStr = _intLiteral(s.bleich_n, "1152921512714829637");
  const ctStr = _intLiteral(s.bleich_target_ct, "0");
  const totalQueries =
    typeof s.bleich_total_queries === "number" ? s.bleich_total_queries : null;
  const recovered = s.bleich_recovered_plaintext || null;
  const modBits =
    typeof s.bleich_modulus_bits === "number" ? s.bleich_modulus_bits : 60;

  const provenance = [];
  if (recovered) {
    provenance.push(`# Page recovered: ${JSON.stringify(recovered)}`);
  }
  if (totalQueries !== null) {
    provenance.push(`# Page used ${totalQueries} oracle queries.`);
  }
  provenance.push(`# Page's modulus was ~${modBits} bits.`);

  return [
    '"""',
    "Bleichenbacher (CRYPTO 1998) attack demo — generated by cloak.moosha.org.",
    "",
    "FOR LEARNING ONLY. Never roll your own RSA, and never use PKCS#1 v1.5",
    "encryption padding for new code. The server below is structurally broken:",
    'by returning a distinguishable "conforming" vs "bad" signal after decrypting',
    "an attacker-supplied ciphertext, it leaks one bit per query — and that one",
    "bit, queried adaptively, lets the attacker recover the plaintext without",
    "ever holding the private key.",
    "",
    "Mitigation: use OAEP padding (PKCS#1 v2.x), or — better — drop RSA key",
    "exchange entirely. TLS 1.3 (2018) is ECDHE-only for exactly this reason.",
    '"""',
    "",
    "from math import gcd",
    "",
    "# ---- toy RSA params ----",
    "#",
    "# Two ~30-bit primes; n = p * q is ~60 bits. For real RSA, n is 2048+ bits",
    "# and Bleichenbacher needs ~10^6 oracle queries (still feasible over a",
    "# network). The toy modulus lets the demo finish in milliseconds.",
    "",
    "P = 1073741827",
    "Q = 1073741831",
    `N = ${nStr}`,
    "E = 17",
    "K = 8  # n is exactly 8 bytes long",
    "",
    "PHI = (P - 1) * (Q - 1)",
    "",
    "",
    "def _egcd(a, b):",
    "    if b == 0:",
    "        return a, 1, 0",
    "    g, x, y = _egcd(b, a % b)",
    "    return g, y, x - (a // b) * y",
    "",
    "",
    "def mod_inv(a, m):",
    "    g, x, _ = _egcd(a % m, m)",
    '    if g != 1:',
    '        raise ValueError(f"no inverse: gcd({a}, {m}) = {g}")',
    "    return x % m",
    "",
    "",
    "D = mod_inv(E, PHI)",
    "",
    "",
    "# ---- the vulnerable PKCS#1 v1.5 decrypt ----",
    "#",
    "# The vulnerability isn't in the format check — the check is correct. The",
    "# vulnerability is *exposing the result* to the attacker as a distinguishable",
    "# response, instead of returning a generic error (or a fake random plaintext,",
    "# as TLS 1.2 countermeasure recommends) for every failure.",
    "",
    "B = 1 << (8 * (K - 2))   # B = 2^{8(k-2)}",
    "",
    "ORACLE_QUERIES = 0       # counter for pedagogical UI",
    "",
    "",
    "def decrypt_pkcs1v15(c: int) -> str:",
    '    """The oracle. Returns "conforming" or "bad".',
    "",
    "    Conforming means the RSA decryption result m, viewed as a big-endian",
    "    k-byte integer, lies in [2B, 3B) — i.e. starts with 0x00 0x02.",
    '    """',
    "    global ORACLE_QUERIES",
    "    ORACLE_QUERIES += 1",
    "    m = pow(c, D, N)",
    "    if 2 * B <= m < 3 * B:",
    '        return "conforming"',
    '    return "bad"',
    "",
    "",
    "# ---- the attack ----",
    "",
    "",
    "def _ceil_div(a, b):",
    "    return (a + b - 1) // b",
    "",
    "",
    "def bleichenbacher(c: int) -> int:",
    '    """Recover m given c = m^e mod n via Bleichenbacher\'s algorithm."""',
    "",
    "    # Step 1: blinding. For this lesson c is already PKCS-conforming, so",
    "    # s0 = 1 suffices. (Real attacks need a sweep here for non-conforming c.)",
    "    s0 = 1",
    "    c0 = c",
    '    assert decrypt_pkcs1v15(c0) == "conforming", "expected target to be conforming"',
    "",
    "    # M = list of disjoint inclusive intervals bounding m * s0 mod n.",
    "    M = [(2 * B, 3 * B - 1)]",
    "    s = _ceil_div(N, 3 * B)",
    "    i = 1",
    "",
    "    while True:",
    "        # Step 2: find next conforming s.",
    "        if i == 1:",
    "            # Step 2.a — first conforming s starting from ceil(n / 3B).",
    "            while True:",
    '                if decrypt_pkcs1v15((c0 * pow(s, E, N)) % N) == "conforming":',
    "                    break",
    "                s += 1",
    "        elif len(M) > 1:",
    "            # Step 2.b — incremental search after multi-interval narrowing.",
    "            s += 1",
    "            while True:",
    '                if decrypt_pkcs1v15((c0 * pow(s, E, N)) % N) == "conforming":',
    "                    break",
    "                s += 1",
    "        else:",
    "            # Step 2.c — narrow single-interval search. Faster than 2.b.",
    "            a, b = M[0]",
    "            r = _ceil_div(2 * (b * s - 2 * B), N)",
    "            found = False",
    "            while not found:",
    "                s_low = _ceil_div(2 * B + r * N, b)",
    "                s_high = (3 * B + r * N) // a",
    "                if s_low > s_high:",
    "                    r += 1",
    "                    continue",
    "                for s_cand in range(s_low, s_high + 1):",
    '                    if decrypt_pkcs1v15((c0 * pow(s_cand, E, N)) % N) == "conforming":',
    "                        s = s_cand",
    "                        found = True",
    "                        break",
    "                if not found:",
    "                    r += 1",
    "",
    "        # Step 3: narrow the intervals.",
    "        new_M = []",
    "        for a, b in M:",
    "            r_low = _ceil_div(a * s - 3 * B + 1, N)",
    "            r_high = (b * s - 2 * B) // N",
    "            for r in range(r_low, r_high + 1):",
    "                lo = max(a, _ceil_div(2 * B + r * N, s))",
    "                hi = min(b, (3 * B - 1 + r * N) // s)",
    "                if lo <= hi:",
    "                    new_M.append((lo, hi))",
    "        # Merge overlapping intervals (rare but possible).",
    "        new_M.sort()",
    "        merged = []",
    "        for iv in new_M:",
    "            if merged and iv[0] <= merged[-1][1] + 1:",
    "                merged[-1] = (merged[-1][0], max(merged[-1][1], iv[1]))",
    "            else:",
    "                merged.append(iv)",
    "        M = merged",
    "",
    '        assert M, f"intervals collapsed at iteration {i}"',
    "",
    "        # Step 4: done?",
    "        if len(M) == 1 and M[0][0] == M[0][1]:",
    "            m0 = M[0][0]",
    "            return (m0 * mod_inv(s0, N)) % N",
    "",
    "        i += 1",
    "",
    "",
    "def _bigint_to_bytes(m: int, k: int) -> bytes:",
    "    return m.to_bytes(k, 'big')",
    "",
    "",
    "def _strip_pkcs1v15(padded: bytes) -> bytes:",
    '    """00 02 PS_nonzero 00 M — return M, raising on bad format."""',
    "    if len(padded) < 4 or padded[0] != 0x00 or padded[1] != 0x02:",
    '        raise ValueError("not PKCS#1 v1.5 conforming")',
    "    sep = padded.find(b\"\\x00\", 2)",
    "    if sep < 3:",
    '        raise ValueError("PKCS#1 v1.5 separator missing or PS empty")',
    "    return padded[sep + 1:]",
    "",
    "",
    'if __name__ == "__main__":',
    "    # The page captured the following from the lesson run:",
    `    TARGET_CT = ${ctStr}`,
    ...(provenance.length ? ["", ...provenance, ""] : [""]),
    "    recovered_int = bleichenbacher(TARGET_CT)",
    "    padded = _bigint_to_bytes(recovered_int, K)",
    "    msg = _strip_pkcs1v15(padded)",
    '    print(f"recovered padded (hex):  {padded.hex()}")',
    '    print(f"recovered message:       {msg.decode(\\"utf-8\\")!r}")',
    '    print(f"oracle queries used:     {ORACLE_QUERIES}")',
    '    print(f"modulus bit length:      {N.bit_length()}")',
    "",
    "    # Sanity: re-encrypt the recovered padded message under the same key",
    "    # and confirm the ciphertext round-trips.",
    "    assert pow(recovered_int, E, N) == TARGET_CT",
    '    print("\\u2713 attack succeeded — no key was ever used by the attacker.")',
  ].join("\n");
}

