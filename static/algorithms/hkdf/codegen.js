// Generates the Python HKDF demo shown on the Done step.
//
// Mirrors what the lesson did in JS: take a "shared secret" stand-in,
// extract a PRK, then expand four independent TLS-style keys from it. We
// use `cryptography.hazmat.primitives.kdf.hkdf.HKDF` — pyca/cryptography's
// HKDF — which exposes the same single-shot Extract+Expand API that Web
// Crypto's `{name:"HKDF"}` gives us. The script also includes a manual
// `HKDFExpand`-only example to show the same PRK feeding multiple Expand
// calls (which is what TLS 1.3 actually does — one Extract, many Expand).
//
// If the page captured the live PRK / four OKMs in state, drop them in as
// trailing comments so the downloaded script and the page cross-reference.

function comment(label, hex) {
  if (!hex) return "";
  return `  # ${label}: ${hex}`;
}

export function full_script(state) {
  const s = state || {};
  const prk        = s.hkdf_prk || "";
  const clientKey  = s.hkdf_client_key || "";
  const serverKey  = s.hkdf_server_key || "";
  const clientIv   = s.hkdf_client_iv || "";
  const serverIv   = s.hkdf_server_iv || "";
  const seedUsed   = s.hkdf_seed_used || "x25519-shared-secret-stand-in";
  const seedLit    = JSON.stringify(seedUsed);

  return [
    "# HKDF in Python — generated by cloak.moosha.org",
    "#",
    "# HKDF (RFC 5869) takes an input keying material (IKM) like a raw",
    "# X25519 shared secret and produces any number of independent output",
    "# keys via Extract-then-Expand. This is the KDF TLS 1.3 uses to derive",
    "# the client/server write keys and IVs from the handshake secret.",
    "#",
    "# Requires the `cryptography` package: pip install cryptography",
    "",
    "from cryptography.hazmat.primitives import hashes",
    "from cryptography.hazmat.primitives.kdf.hkdf import HKDF, HKDFExpand",
    "",
    "# In production, IKM is the 32-byte output of an X25519 (or other DH)",
    "# exchange. Here we stand in for it with the same fixed string the",
    "# lesson used, so the output is reproducible.",
    `IKM  = ${seedLit}.encode("utf-8")`,
    'SALT = b"cloak hkdf demo"  # in TLS 1.3, salt is the previous Secret',
    "",
    "# --- Path 1: single-shot HKDF (Extract + Expand in one call) -----",
    "# Use this when you only need one output of one length. Internally:",
    "#   PRK = HMAC-SHA256(SALT, IKM)                          (Extract)",
    "#   T(1) || T(2) || ... where T(i) = HMAC(PRK, ...)       (Expand)",
    "client_key = HKDF(",
    "    algorithm=hashes.SHA256(),",
    "    length=16,",
    '    salt=SALT,',
    '    info=b"client write key",',
    ")" + ".derive(IKM)" + comment("page captured client_key", clientKey),
    "",
    "# --- Path 2: Extract once, Expand many times ---------------------",
    "# This is the realistic shape: in TLS 1.3 you extract a PRK once and",
    "# expand four (or more) keys from it. pyca/cryptography exposes the",
    "# Expand half on its own via HKDFExpand, but does not expose the",
    "# Extract half as a public API. The equivalent Extract is one HMAC",
    "# call, which we do explicitly here.",
    "import hmac",
    "import hashlib",
    "PRK = hmac.new(SALT, IKM, hashlib.sha256).digest()" +
      comment("page captured PRK", prk),
    "",
    "def expand(info: bytes, length: int) -> bytes:",
    "    return HKDFExpand(",
    "        algorithm=hashes.SHA256(),",
    "        length=length,",
    "        info=info,",
    "    ).derive(PRK)",
    "",
    'client_write_key = expand(b"client write key", 16)' +
      comment("matches page", clientKey),
    'server_write_key = expand(b"server write key", 16)' +
      comment("matches page", serverKey),
    'client_iv        = expand(b"client iv", 12)' +
      comment("matches page", clientIv),
    'server_iv        = expand(b"server iv", 12)' +
      comment("matches page", serverIv),
    "",
    "# Same (PRK, info) → same output every time. Different info → an",
    "# independent, uncorrelated output. That's the property TLS, Signal,",
    "# WireGuard, and Noise all lean on.",
    'print("PRK             :", PRK.hex())',
    'print("client_write_key:", client_write_key.hex())',
    'print("server_write_key:", server_write_key.hex())',
    'print("client_iv       :", client_iv.hex())',
    'print("server_iv       :", server_iv.hex())',
    "",
    "# --- Don't use HKDF as a password hash ---------------------------",
    "# HKDF assumes IKM is already a uniform secret with high entropy",
    "# (like a DH output). It does no deliberate stretching. For passwords",
    "# use PBKDF2 / scrypt / Argon2 instead — they iterate hundreds of",
    "# thousands of times to slow down brute force.",
  ].join("\n");
}

